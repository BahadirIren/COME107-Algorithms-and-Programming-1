#include<iostream>
using namespace std;

int main()
{
	int x = 5;
	cout << "ilk=" << x << endl;
	cout << "x++" << x++ << endl;
	cout << "yeni x=" << x << endl;
	cout << "++x" << ++x << endl;
	cout << "yeni=" << x << endl;
	cout << "--x" << --x << endl;
	cout << "yeni x=" << x << endl;

	system("pause");
	return 0;
}